var status = 0;

function start() {
	status = -1;
	action(1, 0, 0);
}

function action(mode, type, selection) {
	if (mode == -1) {
		cm.dispose();
	} else {
		if (mode == 1)
			status++;
		else
			status--;
		if (status == 0) {
			cm.sendNext("这里景色很不错吧！");
		} else if (status == 1) {
			cm.sendOk("你可以去周围走走，说不定会好惊喜发现！");
			cm.dispose();
		}
	}
}